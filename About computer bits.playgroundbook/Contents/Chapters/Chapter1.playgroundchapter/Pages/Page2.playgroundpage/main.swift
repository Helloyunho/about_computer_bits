 # Bits and Bytes
 So, "a bit" is the smallest unit in a computer,
 and it can only be either 0, or 1.
 
 0 means it's turned off, and 1 means it's turned on!
 
 ---
 
 And when 8 bits are combined, it becomes a byte.
  
